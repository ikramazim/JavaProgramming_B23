package day03_Varibles;

/*
\n: new line
\t: Paragraph space (tab)
\" double quote

 */
public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello everyone \nHow are you all today?");

        System.out.println("**********************************************");

        System.out.println("Hello Everyone");
        System.out.println("My name is Ikram");
        System.out.println("I Live in NNew Jersey");

        System.out.println("******************************");
        System.out.println("Hello Everyone \nMy name is Ikram\nI live in New Jersey");

        System.out.println("**********************************");
        System.out.println("\tToday is a great day to styd Java");

        System.out.println("*************************************");
        System.out.println("My favorite TV is \"Game of Throne\"");

    }


}

