package day03_Varibles;

public class PrintStatementPractice {

    public static void main(String[] args) {


        System.out.print("Hello"); //does not append new line
        System.out.print("Hola"); // does not append new line

        System.out.println("How are you?"); // append new line after printing

        System.out.print("Today is Friday");
        System.out.println("Tomorrow is Saturday");

        System.out.println("____________________________________-");
        System.out.println("\tHello Batch 23\nHow are you Today"); // \n use  to break the line // \t gives paragraph space.
        System.out.println("My name is \"Ikram\"");
        System.out.println("My name is 'name'");

    }
}
