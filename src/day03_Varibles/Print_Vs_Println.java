package day03_Varibles;

public class Print_Vs_Println {

    public static void main(String[] args) {

        System.out.println("Hello Batch 23"); // appends new line after printing
        System.out.println("How are you today?");

        System.out.println("---------------------------------------");

        System.out.print("Hello Btach 23"); // does not append new line
        System.out.print("How are you today");

        System.out.println();
        System.out.println("----------------------------------");
        System.out.println("JRE stands for Java Runtime Environment, Java stands for java Devolopment compiler,");
    }
}
